package org.aiwolf.common;

public class AIWolfRuntimeException extends RuntimeException {

	public AIWolfRuntimeException() {
	}

	public AIWolfRuntimeException(String arg0) {
		super(arg0);
	}

	public AIWolfRuntimeException(Throwable arg0) {
		super(arg0);
	}

	public AIWolfRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AIWolfRuntimeException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
