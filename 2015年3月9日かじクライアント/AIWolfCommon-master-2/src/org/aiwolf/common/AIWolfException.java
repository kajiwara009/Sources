package org.aiwolf.common;

public class AIWolfException extends Exception {

	public AIWolfException() {
	}

	public AIWolfException(String arg0) {
		super(arg0);
	}

	public AIWolfException(Throwable arg0) {
		super(arg0);
	}

	public AIWolfException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AIWolfException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
